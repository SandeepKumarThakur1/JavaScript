// While Loop

let a = 0;
while (a <= 10) {
    // console.log(a)
    a = a + 2;
}

let myArr = [14, 45, 78, 45, 85, 5, 8]
let b = 0;
while (b < myArr.length) {
    // console.log(myArr[b])
    b = b + 1;
}

let c = 1;
while (c <= 10) {
    // console.log(`The Table of - ${c}`)
    let i = 1;
    while (i <= 10) {
        // console.log(c + '*' + i + '=' + c * i)
        i++;
    }
    c++;
}

